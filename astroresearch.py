uploaded = files.upload()
file_name = list(uploaded.keys())[0]
df = pd.read_excel(file_name)

def clean_dec(dec_val):
    try:
        if isinstance(dec_val, pd.Timedelta):
            total_seconds = dec_val.total_seconds()
        else:
            total_seconds = pd.to_timedelta(str(dec_val)).total_seconds()

        sign = '-' if total_seconds < 0 else '+'
        abs_seconds = abs(total_seconds)

        deg = int(abs_seconds // 3600)
        minutes = int((abs_seconds % 3600) // 60)
        seconds = int(abs_seconds % 60)

        return f"{sign}{deg:02}:{minutes:02}:{seconds:02}"
    except Exception:
        return str(dec_val).strip()

df["Dec"] = df["Dec"].apply(clean_dec)
